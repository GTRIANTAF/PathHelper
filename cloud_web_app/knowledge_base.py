def get_total_ects(selected_courses):
    """Επιστρέφει το συνολικό άθροισμα ECTS για τα επιλεγμένα μαθήματα."""
    total_ects = 0
    for target_course in selected_courses:
        course_found = False
        for direction, groups in CEID_COURSES.items():
            for group, courses_dict in groups.items():
                if target_course in courses_dict:
                    total_ects += courses_dict[target_course]["ects"]
                    course_found = True
                    break
            if course_found:
                break
    return total_ects

def get_course_info(course_name):
    """Επιστρέφει το εξάμηνο και τα ECTS ενός συγκεκριμένου μαθήματος."""
    for direction, groups in CEID_COURSES.items():
        for group, courses_dict in groups.items():
            if course_name in courses_dict:
                return courses_dict[course_name]
    return None